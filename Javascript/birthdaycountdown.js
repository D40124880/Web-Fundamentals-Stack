for(var DaysUntilMyBirthday = 60; DaysUntilMyBirthday <= 60; DaysUntilMyBirthday--){
    if(DaysUntilMyBirthday >= 30){
      console.log(DaysUntilMyBirthday + " days until your birthday. Such a long time.");
    }
    else if(DaysUntilMyBirthday >= 5){
      console.log(DaysUntilMyBirthday + " days until your birthday, almost there.");
    }
    else if(DaysUntilMyBirthday >= 1){
      console.log(DaysUntilMyBirthday + " DAYS UNTIL YOUR BIRTHDAY!!!");
    }
    else if(DaysUntilMyBirthday == 0){
      console.log("Today is your Birthday, Have a Party!!!");
    }
  }